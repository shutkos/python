# Напиши програму з двома print-виразами для виводу такого тексту (великі букви «O» та «H», з символів - «#»):
# Зверни увагу, що використання пробілів заборонено, так само як і створення цілого рядка-результата
# за допомогою “”” ”””. Замість цього використовуй символи \n і \t. При перевірці кількість символів у табуляції
# буде дорівнювати 8.
a = '#' * 9
b = '#\t#'
print(a, b, b, b, a, sep='\n', end='\n\n')
print(b, b, a, b, b, sep='\n')
